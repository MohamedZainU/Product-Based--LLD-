public class BookTickets{
    int cusID;
    int cusFlight;
    int cusSeats;
    int price;
    
    
    public BookTickets(int cusID,int cusFlight,int cusSeats,int price){
        this.cusID = cusID;
        this.cusFlight = cusFlight;
        this.cusSeats = cusSeats;
        this.price = price;
    }
    
}