class Books{
    int bookID;
    String bookName;
    String bookReleaseDate;
    int bookAuthor;
    
    public Book(int bookID,String bookName,String bookReleaseDate,int bookAuthor){
        this.bookID = bookID;
        this.bookName = bookName;
        this.bookReleaseDate = bookReleaseDate;
        this.bookAuthor = bookAuthor;
    }
}